/* ดึงค่าอ้างอิงที่อายุ m เดือน (ประมาณเชิงเส้น) */
MB.growthRefAt = function (metric, sex, m) {
  const arr = (MB.GROWTH[metric] || {})[sex];
  if (!arr || !arr.length) return null;
  if (m <= arr[0].m) return arr[0];
  if (m >= arr[arr.length - 1].m) return arr[arr.length - 1];
  for (let i = 0; i < arr.length - 1; i++) {
    const a = arr[i], b = arr[i + 1];
    if (m >= a.m && m <= b.m) {
      const t = (m - a.m) / (b.m - a.m);
      return {
        m,
        lo: a.lo + (b.lo - a.lo) * t,
        mid: a.mid + (b.mid - a.mid) * t,
        hi: a.hi + (b.hi - a.hi) * t
      };
    }
  }
  return arr[arr.length - 1];
};
