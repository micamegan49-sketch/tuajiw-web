/* เกร็ดความรู้ที่เกี่ยวกับช่วงวัยปัจจุบัน (+ ทั่วไป) */
MB.tipsFor = function (stage) {
  return MB.TIPS.filter(t => t.stage === stage || t.stage === 'all');
};
/* เกร็ดประจำวัน (คงที่ตลอดวัน หมุนเปลี่ยนรายวัน) */
MB.tipOfDay = function (arr) {
  if (!arr || !arr.length) return null;
  const d = new Date();
  const doy = Math.floor((d - new Date(d.getFullYear(), 0, 0)) / 86400000);
  return arr[doy % arr.length];
};
