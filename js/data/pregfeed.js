/* คืนการ์ด 3 ใบของ "วันนี้" — เปลี่ยนทุกวันตาม dayIdx (จำนวนวันตั้งครรภ์) และเรียกชื่อลูก */
MB.pregFeed = function (week, dayIdx, name) {
  name = (name && String(name).trim()) || 'ลูกน้อย';
  const pool = (MB.PREG_FEED || []).filter(it =>
    (it.minW == null || week >= it.minW) && (it.maxW == null || week <= it.maxW));
  if (!pool.length) return [];
  const fill = s => String(s).replace(/\{name\}/g, name);
  const n = Math.min(3, pool.length);
  const start = ((Math.floor(dayIdx) % pool.length) + pool.length) % pool.length;
  const out = [];
  for (let i = 0; i < n; i++) {
    const it = pool[(start + i) % pool.length];
    out.push({ em: it.em, label: fill(it.label), text: fill(it.text) });
  }
  return out;
};
